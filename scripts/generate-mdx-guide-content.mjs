import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, dirname, join } from "node:path";

const contentRoot = join(process.cwd(), "content", "guides");
const outputFile = join(process.cwd(), "src", "lib", "generated-mdx-guides.ts");

const entries = readdirSync(contentRoot)
  .filter((file) => file.endsWith(".mdx"))
  .sort((a, b) => a.localeCompare(b))
  .map((file) => {
    const slug = basename(file, ".mdx");
    const source = readFileSync(join(contentRoot, file), "utf8");
    return [slug, parseGuideSource(source)];
  });

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(
  outputFile,
  [
    "// This file is generated by scripts/generate-mdx-guide-content.mjs.",
    "// Do not edit it by hand; edit content/guides/*.mdx instead.",
    "",
    "export const mdxGuides = {",
    ...entries.map(([slug, guide]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(guide)},`),
    "} as const;",
    "",
    "export type MdxGuideSlug = keyof typeof mdxGuides;",
    "",
  ].join("\n"),
);

console.log(`Generated ${outputFile} with ${entries.length} guide(s).`);

function parseGuideSource(source) {
  const match = source.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) {
    throw new Error("Guide is missing frontmatter.");
  }

  return {
    frontmatter: parseFrontmatter(match[1]),
    blocks: parseBlocks(source.slice(match[0].length)),
  };
}

function parseFrontmatter(source) {
  const lines = source.split(/\r?\n/);
  const frontmatter = {};

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    if (!line || line.startsWith("  ")) continue;

    const keyMatch = line.match(/^([A-Za-z][A-Za-z0-9]*):(?:\s*(.*))?$/);
    if (!keyMatch) continue;

    const [, key, rawValue = ""] = keyMatch;
    if (rawValue.trim()) {
      frontmatter[key] = parseScalar(rawValue.trim());
      continue;
    }

    const childLines = [];
    while (lines[index + 1]?.startsWith("  ")) {
      childLines.push(lines[index + 1]);
      index += 1;
    }
    frontmatter[key] = parseFrontmatterChildren(childLines);
  }

  return frontmatter;
}

function parseFrontmatterChildren(lines) {
  if (lines.some((line) => line.trimStart().startsWith("- "))) {
    return parseFrontmatterList(lines);
  }

  const value = {};
  for (const line of lines) {
    const match = line.trim().match(/^([A-Za-z][A-Za-z0-9]*):(?:\s*(.*))?$/);
    if (match) value[match[1]] = parseScalar(match[2] ?? "");
  }
  return value;
}

function parseFrontmatterList(lines) {
  const items = [];

  for (let index = 0; index < lines.length; index += 1) {
    const trimmed = lines[index].trim();
    if (!trimmed.startsWith("- ")) continue;

    const first = trimmed.slice(2);
    const objectMatch = first.match(/^([A-Za-z][A-Za-z0-9]*):(?:\s*(.*))?$/);
    if (!objectMatch) {
      items.push(parseScalar(first));
      continue;
    }

    const item = { [objectMatch[1]]: parseScalar(objectMatch[2] ?? "") };
    while (lines[index + 1]?.startsWith("    ")) {
      const childMatch = lines[index + 1].trim().match(/^([A-Za-z][A-Za-z0-9]*):(?:\s*(.*))?$/);
      if (childMatch) item[childMatch[1]] = parseScalar(childMatch[2] ?? "");
      index += 1;
    }
    items.push(item);
  }

  return items;
}

function parseScalar(value) {
  const trimmed = value.trim();
  if (!trimmed) return "";
  if ((trimmed.startsWith('"') && trimmed.endsWith('"')) || (trimmed.startsWith("'") && trimmed.endsWith("'"))) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
}

function parseBlocks(source) {
  const lines = source.split(/\r?\n/);
  const blocks = [];
  let paragraph = [];

  const flushParagraph = () => {
    if (!paragraph.length) return;
    blocks.push({ type: "paragraph", text: paragraph.join(" ") });
    paragraph = [];
  };

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const trimmed = line.trim();

    if (!trimmed) {
      flushParagraph();
      continue;
    }

    const calloutMatch = trimmed.match(/^<Callout(?:\s+title="([^"]*)")?(?:\s+variant="([^"]*)")?\s*>$/);
    if (calloutMatch) {
      flushParagraph();
      const text = [];
      while (index + 1 < lines.length && lines[index + 1].trim() !== "</Callout>") {
        index += 1;
        if (lines[index].trim()) text.push(lines[index].trim());
      }
      index += 1;
      blocks.push({ type: "callout", title: calloutMatch[1] ?? "", variant: calloutMatch[2] ?? "note", text: text.join(" ") });
      continue;
    }

    if (trimmed.startsWith('<div className="mt-5 flex flex-wrap gap-3">')) {
      flushParagraph();
      const ctas = [];
      while (index + 1 < lines.length && lines[index + 1].trim() !== "</div>") {
        index += 1;
        const ctaMatch = lines[index]
          .trim()
          .match(/^<GuideCta href="([^"]*)"(?:\s+variant="([^"]*)")?>([\s\S]*?)<\/GuideCta>$/);
        if (ctaMatch) ctas.push({ href: ctaMatch[1], variant: ctaMatch[2] ?? "primary", label: ctaMatch[3] });
      }
      index += 1;
      if (ctas.length) blocks.push({ type: "ctas", ctas });
      continue;
    }

    if (trimmed.startsWith("```")) {
      flushParagraph();
      const code = [];
      while (index + 1 < lines.length && !lines[index + 1].trim().startsWith("```")) {
        index += 1;
        code.push(lines[index]);
      }
      index += 1;
      blocks.push({ type: "code", text: code.join("\n") });
      continue;
    }

    const headingMatch = trimmed.match(/^(#{2,3})\s+(.+)$/);
    if (headingMatch) {
      flushParagraph();
      blocks.push({ type: "heading", level: headingMatch[1].length, text: headingMatch[2] });
      continue;
    }

    if (trimmed.startsWith("|")) {
      flushParagraph();
      const rows = [];
      while (index < lines.length && lines[index].trim().startsWith("|")) {
        const cells = lines[index]
          .trim()
          .slice(1, -1)
          .split("|")
          .map((cell) => cell.trim());
        if (!cells.every((cell) => /^:?-{3,}:?$/.test(cell))) rows.push(cells);
        index += 1;
      }
      index -= 1;
      if (rows.length) blocks.push({ type: "table", rows });
      continue;
    }

    const listMatch = trimmed.match(/^(\d+\.|-)\s+(.+)$/);
    if (listMatch) {
      flushParagraph();
      const ordered = listMatch[1] !== "-";
      const items = [listMatch[2]];
      while (index + 1 < lines.length) {
        const nextMatch = lines[index + 1].trim().match(/^(\d+\.|-)\s+(.+)$/);
        if (!nextMatch || (nextMatch[1] !== "-" ? true : false) !== ordered) break;
        index += 1;
        items.push(nextMatch[2]);
      }
      blocks.push({ type: "list", ordered, items });
      continue;
    }

    paragraph.push(trimmed);
  }

  flushParagraph();
  return blocks;
}
